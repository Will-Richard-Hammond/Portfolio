using MGGameLibrary.Graphs;
using Microsoft.Xna.Framework;
using Microsoft.Xna.Framework.Graphics;

namespace MainQuest5_PacManGhost
{
    /// <summary>
    /// Base class for all screen states. Extends DrawableGameComponent so each
    /// state has access to the GraphicsDevice, and implements IState via abstract methods.
    /// </summary>
    public abstract class ScreenState : DrawableGameComponent, IState
    {
        public ScreenState(Game game) : base(game) { }

        public abstract void OnEnter();
        public abstract void OnExit();
        public abstract void OnUpdate(float seconds);
        public abstract override void Draw(GameTime gameTime);
    }

    // ??????????????????????????????????????????????????????????????????????????
    // Flash screen Ś shows a logo for a set time then auto-advances
    // ??????????????????????????????????????????????????????????????????????????
    public class FlashScreenState : ScreenState
    {
        private readonly Texture2D _logo;
        private SpriteBatch _spriteBatch;
        public Timer Timer { get; }

        public FlashScreenState(float duration, Texture2D logo, Game game) : base(game)
        {
            _logo = logo;
            Timer = new Timer(duration);
        }

        public override void OnEnter() => Timer.Reset();
        public override void OnExit() { }

        public override void OnUpdate(float seconds) => Timer.Update(seconds);

        public override void Draw(GameTime gameTime)
        {
            _spriteBatch ??= new SpriteBatch(GraphicsDevice);
            GraphicsDevice.Clear(Color.Black);
            _spriteBatch.Begin();
            _spriteBatch.Draw(_logo, new Vector2(10, 10), Color.White);
            _spriteBatch.End();
        }
    }

    // ??????????????????????????????????????????????????????????????????????????
    // Title screen Ś press Space to play, press C for credits
    // ??????????????????????????????????????????????????????????????????????????
    public class TitleScreenState : ScreenState
    {
        public TitleScreenState(Game game) : base(game) { }

        public override void OnEnter() { }
        public override void OnExit() { }
        public override void OnUpdate(float seconds) { }

        public override void Draw(GameTime gameTime)
        {
            GraphicsDevice.Clear(Color.Teal);
        }
    }

    // ??????????????????????????????????????????????????????????????????????????
    // Credits screen Ś press C to return to title
    // ??????????????????????????????????????????????????????????????????????????
    public class CreditsScreenState : ScreenState
    {
        public CreditsScreenState(Game game) : base(game) { }

        public override void OnEnter() { }
        public override void OnExit() { }
        public override void OnUpdate(float seconds) { }

        public override void Draw(GameTime gameTime)
        {
            GraphicsDevice.Clear(Color.DarkOliveGreen);
        }
    }

    // ??????????????????????????????????????????????????????????????????????????
    // Game screen Ś press P to pause, press C (cheat) to trigger game over
    // ??????????????????????????????????????????????????????????????????????????
    public class GameScreenState : ScreenState
    {
        /// <summary>False when the game has ended and the FSM should move to GameOver.</summary>
        public bool Playing { get; private set; } = true;

        public GameScreenState(Game game) : base(game) { }

        public override void OnEnter() => Playing = true;
        public override void OnExit() { }
        public override void OnUpdate(float seconds) { }

        public override void Draw(GameTime gameTime)
        {
            GraphicsDevice.Clear(Color.DarkSlateBlue);
        }
    }

    // ??????????????????????????????????????????????????????????????????????????
    // Pause screen Ś press P to resume
    // ??????????????????????????????????????????????????????????????????????????
    public class PauseScreenState : ScreenState
    {
        public PauseScreenState(Game game) : base(game) { }

        public override void OnEnter() { }
        public override void OnExit() { }
        public override void OnUpdate(float seconds) { }

        public override void Draw(GameTime gameTime)
        {
            GraphicsDevice.Clear(Color.DarkGoldenrod);
        }
    }

    // ??????????????????????????????????????????????????????????????????????????
    // Game-over screen Ś displays briefly then returns to title
    // ??????????????????????????????????????????????????????????????????????????
    public class GameOverScreenState : ScreenState
    {
        private readonly GameScreenState _gameScreen;
        public Timer Timer { get; }

        public GameOverScreenState(float duration, GameScreenState gameScreen, Game game) : base(game)
        {
            _gameScreen = gameScreen;
            Timer = new Timer(duration);
        }

        public override void OnEnter() => Timer.Reset();
        public override void OnExit() { }
        public override void OnUpdate(float seconds) => Timer.Update(seconds);

        public override void Draw(GameTime gameTime)
        {
            GraphicsDevice.Clear(Color.DarkRed);
        }
    }
}
